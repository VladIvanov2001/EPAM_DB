DEFINE DATA_TBS_NAME = &&1
DEFINE CURS = '_DATA'

CREATE TABLE PLAYLIST
(
  ID_PLAYLIST INT GENERATED ALWAYS AS IDENTITY INCREMENT BY 1 START WITH 1 MINVALUE 1 NOMAXVALUE NOT NULL 
, PLAYLIST_BY_USER CHAR(1) 
, PLAYLIST_BY_SYSTEM CHAR(1) 
, PRIVATE_PLAYLIST CHAR(1) 
, PUBLIC_PLAYLIST CHAR(1) 
, CONSTRAINT PLAYLIST_PK PRIMARY KEY 
  (
    ID_PLAYLIST 
  )
  ENABLE 
)
TABLESPACE &&DATA_TBS_NAME&&CURS;

COMMENT ON COLUMN PLAYLIST.ID_PLAYLIST IS 'primary key';

COMMENT ON COLUMN PLAYLIST.PLAYLIST_BY_USER IS 'boolean type. This type created by person';

COMMENT ON COLUMN PLAYLIST.PLAYLIST_BY_SYSTEM IS 'boolean type. This type created by system';

COMMENT ON COLUMN PLAYLIST.PRIVATE_PLAYLIST IS 'boolean type. This is private type, which you cant see';

COMMENT ON COLUMN PLAYLIST.PUBLIC_PLAYLIST IS 'boolean type. This is public type, whick can be used by all users';